    public function report(Request $request)
    {
        $post = new Report;
        $post -> reportedBy = auth()->user()->id;
        $post -> reportUserId = $request->input('userid');
        $post -> reportPostId = $request->input('postid');
        $post -> reportCommentId = $request->input('commentid');
        $post -> reportDescription = $request->input('reportDescription');
        $post -> reportStatus = "PROCESS";
        $post->save();

        return redirect()->back();
    }
